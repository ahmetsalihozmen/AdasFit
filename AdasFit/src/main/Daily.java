package main;

public class Daily {
	public int dailyFat = 0;
	public int dailyCarb = 0;
	public int dailyProtein = 0;
	public int dailyCal = 0;
	
	
	public Daily(int dailyFat, int dailyCarb, int dailyProtein, int dailyCal) {
		super();
		this.dailyFat = dailyFat;
		this.dailyCarb = dailyCarb;
		this.dailyProtein = dailyProtein;
		this.dailyCal = dailyCal;
	}
	
	
	
	
}
